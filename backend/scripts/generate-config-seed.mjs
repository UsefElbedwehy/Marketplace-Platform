#!/usr/bin/env node
// Generates backend/supabase/seed/00_reference_config.sql from configs/**.
//
// The *seeding mechanism* stays pure SQL (matching supabase/config.toml's
// db.seed.sql_paths and how `supabase db reset` seeds any real Supabase
// project) — this script only regenerates that SQL file from the source
// configs, following the exact merge model documented in
// contract/README.md#development-schema-merge-model (default <- client
// overlay <- env overlay). Re-run it whenever configs/** changes:
//
//   node backend/scripts/generate-config-seed.mjs
//
// then re-apply via backend/scripts/local-db-reset.sh (local) or
// `supabase db reset` (once Docker is available).

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { deepMerge } from "../../packages/config-validation/src/merge.js";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(__dirname, "..", "..");
const CLIENTS_DIR = path.join(REPO_ROOT, "configs", "clients");
const ENV_DIR = path.join(REPO_ROOT, "configs", "development");
const OUT_PATH = path.join(REPO_ROOT, "backend", "supabase", "seed", "00_reference_config.sql");

// Stable, hand-assigned tenant ids so re-running this script is idempotent
// against a fixed seed — not derived from anything, just fixture constants.
const TENANT_IDS = {
  default: "00000000-0000-0000-0000-000000000001",
  client_a: "00000000-0000-0000-0000-000000000002",
};

function readJson(filePath) {
  return JSON.parse(fs.readFileSync(filePath, "utf-8"));
}

function sqlDollarQuote(value) {
  return `$json$${JSON.stringify(value)}$json$`;
}

function buildTenantDocs(clientId) {
  const clientDir = path.join(CLIENTS_DIR, clientId);
  const defaultDir = path.join(CLIENTS_DIR, "default");

  const isDefault = clientId === "default";
  let config = readJson(path.join(defaultDir, "config.json"));
  let theme = readJson(path.join(defaultDir, "theme.json"));

  if (!isDefault) {
    config = deepMerge(config, readJson(path.join(clientDir, "config.json")));
    theme = deepMerge(theme, readJson(path.join(clientDir, "theme.json")));
  }

  const envConfigPath = path.join(ENV_DIR, "config.json");
  if (fs.existsSync(envConfigPath)) {
    config = deepMerge(config, readJson(envConfigPath));
  }

  return { config, theme };
}

function main() {
  const clientIds = fs
    .readdirSync(CLIENTS_DIR, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name)
    .sort();

  const lines = [
    "-- AUTO-GENERATED by backend/scripts/generate-config-seed.mjs — do not edit by hand.",
    "-- Regenerate: node backend/scripts/generate-config-seed.mjs",
    "-- Seeds one platform.tenant + one active config.bundle + one active config.theme",
    "-- row per configs/clients/<client>, merged per contract/README.md's merge model",
    "-- (default <- client overlay <- development env overlay).",
    "",
  ];

  for (const clientId of clientIds) {
    const tenantId = TENANT_IDS[clientId];
    if (!tenantId) {
      throw new Error(`No fixture tenant id assigned for client "${clientId}" — add one to TENANT_IDS.`);
    }
    const { config, theme } = buildTenantDocs(clientId);

    lines.push(`-- ${clientId}`);
    lines.push(
      `insert into platform.tenant (id, slug, display_name) values ('${tenantId}', '${clientId}', '${(config.identity?.legalName ?? clientId).replace(/'/g, "''")}');`
    );
    lines.push(
      `insert into config.bundle (tenant_id, version, document, is_active) values ('${tenantId}', 1, ${sqlDollarQuote(config)}::jsonb, true);`
    );
    lines.push(
      `insert into config.theme (tenant_id, version, document, is_active) values ('${tenantId}', 1, ${sqlDollarQuote(theme)}::jsonb, true);`
    );
    lines.push("");
  }

  fs.mkdirSync(path.dirname(OUT_PATH), { recursive: true });
  fs.writeFileSync(OUT_PATH, lines.join("\n") + "\n");
  console.log(`Wrote ${path.relative(REPO_ROOT, OUT_PATH)}`);
}

main();
